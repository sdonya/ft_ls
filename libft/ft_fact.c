#include "libft.h"

int		ft_fact(int nb)
{
	if (0 > nb || nb > 12)
	{
		return (0);
	}
	if (nb == 1 || nb == 0)
	{
		return (1);
	}
	else
	{
		nb = nb * ft_fact(nb - 1);
	}
	return (nb);
}
